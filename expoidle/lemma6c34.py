recip_pi = 0.318309886
recip_e = 0.367879441
lowest = 100

for p in range(2, 100):
    c3 = p ** recip_e

    c4, l = 0, 0
    while c3 - (l + 1) ** recip_pi> 0:
        l += 1
        c4 = l ** recip_pi
        
    val = c3 - c4
    if val < lowest:
        print(f"{lowest}: c3={p}, c4 = {l}")
        lowest = val


